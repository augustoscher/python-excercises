import numpy as np
print('--- Operações com Numpy arrays ---')

# Operação vetorizada: Soma cada elemento com o segundo array
array1 = np.arange(0, 20)
print(array1)
print()
print('Soma: ')
array2 = array1 + array1
print(array2)

# Suporta todas as operações
print('Multiplicacao: ')
array3 = array1 * array1
print(array3)
print()

